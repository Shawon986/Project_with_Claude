export function useI18n() {
  const { state } = useAppStore()
  const t = (key, params = {}) => {
    const msg = messages[state.locale]?.[key] || messages.en[key] || key
    if (typeof msg === 'string' && params) {
      return msg.replace(/\{(\w+)\}/g, (_, k) => params[k] ?? `{${k}}`)
    }
    return msg
  }
  const locale = computed(() => state.locale)
  return { t, locale }
}
